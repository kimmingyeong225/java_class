package basic.ch16;

public class Calculator {
	
	public int add(int a, int b) {
		return a+ b;
	}
	
	public double add(double a, double b) {
		return a + b;
	}
	
	public int add(int a, int b, int c) {
		return a + b  + c;
	}
	
	public String add(String str1, String str2) {
		return str1 + str2;
	}
	
}//end of class
